  constructor() {
    const saved = localStorage.getItem('language') as LanguageCode | null;
    this.setLanguage(saved || this.fallbackLanguage, false);
  }

  currentLanguage(): LanguageCode {
    return this.language();
  }

  setLanguage(language: string, persist = true): void {
    const normalized = this.isSupportedLanguage(language) ? language : this.fallbackLanguage;
    this.language.set(normalized);
    document.documentElement.setAttribute('lang', normalized);
    if (persist) {
      localStorage.setItem('language', normalized);
    }
  }

  t(key: string, params?: Record<string, string | number>): string {
    const lang = this.language();
    const value =
      this.translations[lang][key] ??
      this.translations[this.fallbackLanguage][key] ??
      key;

    if (!params) {
      return value;
    }

    return Object.entries(params).reduce(
      (acc, [paramKey, paramValue]) => acc.replace(`{${paramKey}}`, String(paramValue)),
      value
    );
  }

  getLanguageOptions(): Array<{ code: LanguageCode; label: string; locale: string }> {
    return [
      { code: 'en', label: this.t('language.en'), locale: 'en-US' },
      { code: 'sw', label: this.t('language.sw'), locale: 'sw-KE' },
      { code: 'fr', label: this.t('language.fr'), locale: 'fr-FR' }
    ];
  }

  getLocaleFor(language: string): string {
    const option = this.getLanguageOptions().find((entry) => entry.code === language);
    return option?.locale || 'en-US';
  }

  private isSupportedLanguage(language: string): language is LanguageCode {
    return ['en', 'sw', 'fr'].includes(language);
  }
